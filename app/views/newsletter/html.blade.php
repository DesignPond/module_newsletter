                            <p style="text-align:justify;font-family:Arial, Helvetica, sans-serif;font-size:12px;font-weight:normal;color:#303030;margin:0 0 10px 0;padding:0;">La Suisse est un pays de locataires. L’évolution de la société tend vers une multiplication
                                des familles. Une utilisation toujours plus mesurée du sol et des contraintes administratives strictes s’additionnent
                                pour péjorer l’offre de logements. D’où une situation tendue entre bailleurs et locataires. Cet abrégé se veut une
                                étude illustrative de ce droit social qu’est devenu le droit du bail. Il consacre une large place à la jurisprudence,
                                laquelle est abondante. L’accent est également mis sur l’aspect procédural, indissociable d’une juste application
                                du droit matériel. Contenu limité à l’étude des baux commerciaux et d’habitations, l’ouvrage traite de la relation
                                bailleur-locataire dès le début du bail jusqu’à son extinction.</p>
                            <p style="text-align:justify;font-family:Arial, Helvetica, sans-serif;font-size:12px;font-weight:normal;color:#303030;margin:0 0 10px 0;padding:0;">
                                Sont ainsi abordées notamment les questions touchant la conclusion du contrat, la pluralité de locataires,
                                l’entretien et les défauts de la chose louée, la consignation des loyers, les frais accessoires, la sous-location
                                et le transfert de bail, la restitution de la chose louée et son aliénation, la problématique complexe de la
                                fixation des loyers et des congés. Enfin, l’ouvrage aurait été incomplet s’il n’avait pas tenu compte des
                                règles procédurales spécifiques en la matière.</p>
